function multi(p1, p2) {
    return p1 * p2; // p1, p2 곱연산의 결과를 반환한다.
}

function div(p1, p2) {
    return p1 / p2; // p1, p2 나눗셈연산의 결과를 반환한다.
}

function plus(p1, p2) {
    return p1 + p2; // p1, p2 덧셈연산의 결과를 반환한다.
}

function minus(p1, p2) {
    return p1 - p2; // p1, p2 뺄셈연산의 결과를 반환한다.
}

// 결과 출력
console.log(multi(2,3))
console.log(div(2,3))
console.log(plus(2,3))
console.log(minus(2,3))
